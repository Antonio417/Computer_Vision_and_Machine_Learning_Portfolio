# Concept of KNN 
# A sample is classified by a popularity vote of its nearest neighbors
# Steps:
# 1. Measure the distance between the new sample and the train sample using euclidean distance
# 2. Look for the nearest train sample
# 3. Predict the class of our new sample based on the most common label 
#    of their neighbors
# K means the number of neighbor samples that are used
# So if k = 6, means that we'll predict our new sample based on 6 neighbors and look at their 
# classes

import numpy as np
from collections import Counter

def euclidean_distance(x1, x2): # To measure the distance between the new sample and the train sample
    return np.sqrt(np.sum((x1 - x2) ** 2))

class KNN:
    def __init__(self, k=5):
        self.k = k

    def fit(self, X, y):
        self.X_train = X
        self.y_train = y

    def predict(self, X):
        y_pred = [self._predict(x) for x in X]
        return np.array(y_pred) # turn list into an array

    def _predict(self, x):
        # Compute distances between x and all examples in the training set
        distances = [euclidean_distance(x, x_train) for x_train in self.X_train]
        # Sort by distance and return indices of the first k neighbors
        k_idx = np.argsort(distances)[: self.k]
        # Extract the labels of the k nearest neighbor training samples
        k_neighbor_labels = [self.y_train[i] for i in k_idx]
        # return the most common class label
        most_common = Counter(k_neighbor_labels).most_common(1)
        return most_common[0][0]

